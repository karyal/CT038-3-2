public class SwitchCase {
    public static void main(String  []args){
        int num1 = 3; //read from user
        switch(num1){
            case 1:
                System.out.println("SUN");
                break;
            case 2:
                System.out.println("MON");
                break;
            case 3:
                System.out.println("TUE");
                break;
            default:
                System.out.println("Others");
        }
        
    }
}
