package week6;

public class Class1 {
    private int num1;
    //constructors
    public Class1(){
        this.num1=0;
    }
    public Class1(int num1){
        this.num1=num1;
    }
    //setters
    public void setNum1(int num1){
        this.num1=num1;
    }
    //getters
    public int getNum1(){
        return this.num1;
    }
    //toString
    public String toString(){
        return this.num1+"";
    }
}
